# Cuanto es el X porciento de x número

# Solicito el número del cual se quiere obtener el porcentaje
numPorcentaje = int(input("¿De que número quieres el porcentaje?: "))

# Luego solicito el valor del porcentaje requerido 
valPorcentaje = int(input("¿Qué porcentaje quieres?: "))

# Se realiza la operación
res = numPorcentaje * valPorcentaje / 100

# Se muestra el mensaje con el resultado esperado
print(f"El valor del porcentaje que requieres es: {res}")

